class Replacing {
    public static void main(String[] args) {
	String inventor = "Charles Babbage";
	System.out.println(inventor);
	String inventor_c = inventor.replace('B', 'C');
	System.out.println(inventor_c);
	String inventor_o = inventor.replace('a', 'o');
	System.out.println(inventor_o);
	System.out.println(inventor);
    }
}
