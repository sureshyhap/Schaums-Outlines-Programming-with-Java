class TestStringClass {
    public static void main(String[] args) {
	String name = "James Augustus Yhap";
	System.out.println("My father's name is: " + name);
	System.out.println("The length is: " + name.length());
	System.out.println("The character at index 4 is: " + name.charAt(4));
	System.out.println("The index of the character Y is: " + name.indexOf('Y'));
	System.out.println("The lowercase version is: " + name.toLowerCase());
	System.out.println("My father's name is still: " + name);
    }
}
